#define FONT_FIRST_CHAR 32
#define FONT_CHAR_W 8
#define FONT_CHAR_H 8

static inline const uint8_t *font_get_glyph(char c) {
    int idx = (unsigned char)c - FONT_FIRST_CHAR;
    if (idx < 0 || idx >= (int)(sizeof(font_8x8) / sizeof(font_8x8[0])))
        idx = 0; // space for out-of-range
    return font_8x8[idx];
}

